  <h2>Generalità</h2>
  <p><br>
    Il ciclismo su pista si svolge in un velodromo con due rettilinei e due curve. Per mantenere le alte velocità anche in curva
    sono state progettate le curve paraboliche, ovvero un rialzamento dell'inclinazione della curva.<br>
    I ciclisti utilizzano biciclette con ruota fissa, ovvero i pedali girano sempre anche quando la gara è finita e vogliamo riposarci!<br>
    Le biciclette non hanno freni ma non per questo non si puo frenare. Per frenare occorre imprimere una forza contraria alla marcia 
    in avanti e come per magia la bici rallenterà. <br>
    Per i principianti occorreranno diverse ore di pratica per adattarsi a questo nuovo mondo.<br>
    Altra cosa le bici da pista non hanno il cambio ma hanno un pignone posteriiore e una corona anteriore.
  </p><br><br>
  <h2>Passista o velocista</h2>
  <p><br>
    La categoria di appartenenza di un ciclista determina il tipo di specialità che dovrà intraprendere nella pista.<br>
    Fondamentalmente la pista si divide in due categorie: resistenza e velocità.<br>
    Chi ha doti di resistenza potrà disputare gare cronometriche o inseguimenti, dove lo sforzo si prolungherà per minuti e non 
    per secondi.
    Chi invece ha la facoltà di sprigionare in pochi secondi una forza estrema sui pedali, potrà cimentarsi con gare di velocità 
    individuali o a squadre.<br><br>
    <b style="background-color:rgb(33, 224, 132);color:rgb(16, 16, 123);border-radius:25px;padding:2px">
        Velocità individuale:</b><br>
    I concorrenti gareggiano in coppia uno contro uno in attesa dello sprint finale. Possono decidere di lanciare la volata quando
    vogliono e spesso cercano di sorprendere l'avversario per avere quei 2 metri di vantaggio che possono permettere la vittoria.<br>
    Esporsi toppo presto al vento però può portare a perdere velocità con il rischio di essere sorpassati dall'avversario negli ultimi
    10 metri. La tattica gioca un ruolo fondamentale in questa disciplina.<br> <br>
    <b style="background-color:rgb(33, 224, 132);color:rgb(16, 16, 123);border-radius:25px;padding:2px">Velocità a squadre</b><br>
    In questo caso vince la squadra che finisce prima dell'altra in un certo numero di giri.<br>
    Le due squadre non partono dallo stesso punto ma da due lati opposti della pista. Nella categoria maschile ci sono tre atleti 
    per squadra ed effettuano tre giri mentre nella categoria femminile abbiamo due atlete per squadra ed i giri sono due.<br>
    E' importante partire subito forte pedalando al massimo e trovare la coordinazione con i compagni di squadra per ottimizzare
    i benefici aerodinamici che si hanno stando a ruota.<br>
    Oltre alla forza bisogna anche avere l'intuizione di mantenere un'andatura e una frequenza di cambi in base alla situazione 
    fisica dei propri compagni di squadra.<br><br>
    <b style="background-color:rgb(33, 224, 132);color:rgb(16, 16, 123);border-radius:25px;padding:2px">Kerin</b><br>
    In questa specialità gli atleti possono essere al massimo 6 e devono percorrere 1500 metri di cui la metà (750 mt) sono pedalati 
    dietro ad un motorino che aumenta l'andatura fino ad arrivare a 50 Km/h per le gare maschili e i 45 per quelle femminili.<br>
    I ciclisti cercheranno di prendere le migliori posizioni fino a quando, a 750 mt all'arrivo, il motorino si scosta e gli atleti
    si giocheranno la vittoria in una lungo volata.<br>
    I primi due o tre atleti di ogni gara potranno accedere al prossimo turno mentre gli altri saranno eliminati.<br><br>
    <b style="background-color:rgb(33, 224, 132);color:rgb(16, 16, 123);border-radius:25px;padding:2px">Inseguimento a squadre</b><br>
    In questa prova di resistenza ogni squadra è composta da quattro ciclisti che partono da due lati opposti della pista.<br>
    La gara dura quattro chilometri e vince chi impiega meno tempo per coprire la distanza da percorrere.
    Può accadere che una delle due squadre raggiunge l'altra vincendo la gara. 
    L’inseguimento a squadre è una prova di resistenza ed è importante mantenere un’alta velocità ed effettuare cambi rapidi.
    Bisogna evitare di dare strappi eccessivi all'andatura, tali da costringere i propri compagni a perdere la scia e distaccarsi dalla
    ruota del compagno che lo precede.<br>
    In questa specialità la tattica è semplice: dopo aver tirato in testa per diversi secondi ci si fa superare, ci si rimette in scia 
    e si aspetta di nuovo il proprio turno. Il tempo viene preso sul terzo corridore che finisce di percorrere tutti i quattro chilometri.<br><br>
    <b style="background-color:rgb(33, 224, 132);color:rgb(16, 16, 123);border-radius:25px;padding:2px">Madison</b><br>
    E' simile alla staffetta e si gareggia a coppie. La categoria maschile percorrono 50 km me mentre la categoria femminile percorrere
    30 km. L'atleta percorre il giro di pista e quando raggiunge il proprio compagnio di squadra lo deve toccare nella mano.
    Quest'ultimo potrà partire solo quando il compagno che sopraggiunge a tutta velocità effettua questa mossa.
    Ci vogliono delle doti di acrobata perchè nel momento in cui l'atleta tocca il proprio compagno, in realtà gli afferra la
    mano e lo lancia letteralmente in avanti. In parole tecniche trasferisce il suo moto veloce a quello del compagno che sarà piu lento.<br>
    Ogni 10 giri c'è la volata per prendere i punti: 5 punti al primo, 3 al secondo, 2 al terzo e 1 al quarto.<br>
    Nel caso in cui un corridore riesce a prendere un intero giro di vantaggio sul gruppo avrà l'abbuono di 20 punti.<br><br>
